    private void seedCategoryTaxonomy(CategoryTaxonomyRepository categoryTaxonomyRepository) {
        for (CategoryOutline outline : categoryOutlines()) {
            if (!categoryTaxonomyRepository.existsBySubjectAndChapterNameAndKnowledgeTagIsNull(outline.subject(), outline.chapterName())) {
                categoryTaxonomyRepository.save(taxonomy(
                        outline.subject(),
                        outline.subjectOrder(),
                        outline.chapterName(),
                        outline.chapterOrder(),
                        null,
                        null
                ));
            }
            for (int i = 0; i < outline.knowledgeTags().size(); i++) {
                String knowledgeTag = outline.knowledgeTags().get(i);
                if (categoryTaxonomyRepository.existsBySubjectAndChapterNameAndKnowledgeTag(
                        outline.subject(),
                        outline.chapterName(),
                        knowledgeTag
                )) {
                    continue;
                }
                categoryTaxonomyRepository.save(taxonomy(
                        outline.subject(),
                        outline.subjectOrder(),
                        outline.chapterName(),
                        outline.chapterOrder(),
                        knowledgeTag,
                        i + 1
                ));
            }
        }
    }

    private void normalizeQuestionCategories(QuestionRepository questionRepository) {
        List<Question> questions = questionRepository.findAll();
        boolean changed = false;
        for (Question question : questions) {
            changed |= remapQuestion(question);
        }
        if (changed) {
            questionRepository.saveAll(questions);
        }
    }

