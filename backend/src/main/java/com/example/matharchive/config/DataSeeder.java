    @Bean
    CommandLineRunner seedData(
            AppUserRepository appUserRepository,
            QuestionRepository questionRepository,
            FavoriteRepository favoriteRepository,
            MistakeRecordRepository mistakeRecordRepository,
            CategoryTaxonomyRepository categoryTaxonomyRepository,
            PracticeRecordRepository practiceRecordRepository,
            OperationLogRepository operationLogRepository,
            SystemSettingRepository systemSettingRepository
    ) {
        return args -> {
            seedUsers(appUserRepository);
            cleanupAutoGeneratedQuestions(
                    questionRepository,
                    favoriteRepository,
                    mistakeRecordRepository,
                    practiceRecordRepository
            );
            cleanupAutoGeneratedOperationLogs(operationLogRepository);
            normalizeQuestionCategories(questionRepository);
            seedCategoryTaxonomy(categoryTaxonomyRepository);
            seedSettings(systemSettingRepository);
        };
    }

    private void seedUsers(AppUserRepository appUserRepository) {
        appUserRepository.findByUsername("admin").ifPresent(user -> {
            if (!hasText(user.getPhone())) {
                user.setPhone("13800000000");
            }
            if (user.getLastLoginAt() == null) {
                user.setLastLoginAt(LocalDateTime.now().minusHours(2));
            }
            appUserRepository.save(user);
        });

        appUserRepository.findByUsername("student_01").ifPresent(user -> {
            if (!hasText(user.getPhone())) {
                user.setPhone("13900001111");
            }
            if (user.getLastLoginAt() == null) {
                user.setLastLoginAt(LocalDateTime.now().minusDays(1));
            }
            appUserRepository.save(user);
        });
    }

    private void seedQuestions(QuestionRepository questionRepository) {
        for (Question item : generatedQuestions()) {
            if (isAutoGeneratedSeedQuestion(item)) {
                continue;
            }
            if (questionRepository.existsByArchiveCode(item.getArchiveCode())) {
                continue;
            }
            questionRepository.save(item);
        }
    }

    private void cleanupAutoGeneratedQuestions(
            QuestionRepository questionRepository,
            FavoriteRepository favoriteRepository,
            MistakeRecordRepository mistakeRecordRepository,
            PracticeRecordRepository practiceRecordRepository
    ) {
        for (Question question : questionRepository.findAll()) {
            if (!isAutoGeneratedSeedQuestion(question)) {
                continue;
            }
            Long questionId = question.getId();
            favoriteRepository.deleteByQuestion_Id(questionId);
            mistakeRecordRepository.deleteByQuestion_Id(questionId);
            practiceRecordRepository.deleteByQuestion_Id(questionId);
            questionRepository.delete(question);
        }
    }

    private void cleanupAutoGeneratedOperationLogs(OperationLogRepository operationLogRepository) {
        List<OperationLog> generatedLogs = operationLogRepository.findAll().stream()
                .filter(this::isAutoGeneratedOperationLog)
                .toList();
        if (!generatedLogs.isEmpty()) {
            operationLogRepository.deleteAll(generatedLogs);
        }
    }

