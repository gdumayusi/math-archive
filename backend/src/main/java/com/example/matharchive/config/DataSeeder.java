    private boolean hasText(String value) {
        return value != null && !value.isBlank();
    }

    private boolean isAutoGeneratedSeedQuestion(Question question) {
        return question != null && isAutoGeneratedSeedQuestion(question.getArchiveCode());
    }

    private boolean isAutoGeneratedSeedQuestion(String archiveCode) {
        return AUTO_GENERATED_QUESTION_CODES.contains(archiveCode) || isLegacySimulationQuestion(archiveCode);
    }

    private boolean isLegacySimulationQuestion(String archiveCode) {
        if (!hasText(archiveCode)) {
            return false;
        }
        return LEGACY_SIMULATION_PREFIXES.stream().anyMatch(archiveCode::startsWith);
    }

    private boolean isAutoGeneratedOperationLog(OperationLog log) {
        if (log == null) {
            return false;
        }
        String detail = log.getActionDetail();
        if (!hasText(detail)) {
            return false;
        }
        if ("初始化数据".equals(log.getActionType())) {
            return true;
        }
        if ("创建系统默认配置".equals(detail)) {
            return true;
        }
        for (String archiveCode : AUTO_GENERATED_QUESTION_CODES) {
            if (detail.contains(archiveCode)) {
                return true;
            }
        }
        for (String prefix : LEGACY_SIMULATION_PREFIXES) {
            if (detail.contains(prefix)) {
                return true;
            }
        }
        return false;
    }

